import { app, BrowserWindow, Menu, shell, ipcMain } from 'electron';
import { isUndefined } from 'lodash';
import { saveFile, openFile, triggerSave } from './utils/files';
import {
  REGISTER_MAIN, REGISTER, TRIGGER_WINDOW_ACTION, DATA_READY_FOR_SAVE,
  TRIGGER_FILE_OPEN, GOT_TOKEN, TRIGGER_LOGIN
} from './utils/consts';
import pkg from './package.json';

let menu;
let template;
let mainWindow = null;
let renderer = null;
let mainRenderer = null;

ipcMain.on(REGISTER_MAIN, (event) => {
  mainRenderer = event.sender;
});

ipcMain.on(REGISTER, (event) => {
  renderer = event.sender;
});

ipcMain.on(DATA_READY_FOR_SAVE, () => {
  saveFile(() => {}, (c) => c);
});

ipcMain.on(TRIGGER_FILE_OPEN, (event, argString) => {
  const { filters } = JSON.parse(argString);
  openFile(mainRenderer, renderer, filters);
});

ipcMain.on(TRIGGER_LOGIN, () => {
  const authWindow = new BrowserWindow({ width: 800, height: 600, show: false, 'node-integration': false });
  authWindow.loadURL('http://localhost:3333/users/auth/facebook');

  authWindow.webContents.on('did-finish-load', () => {
    mainWindow.show();
  });

  authWindow.webContents.on('did-get-redirect-request', (event, oldUrl, newUrl) => {
    const rawToken = /t=([^&]*)/.exec(newUrl) || null;
    const token = (rawToken && rawToken.length > 1) ? rawToken[1] : null;

    if (token) {
      // Close the browser if token found or error
      if (mainRenderer !== null) {
        mainRenderer.send(GOT_TOKEN, token);
      }
    }
  });
});

if (process.env.NODE_ENV === 'production') {
  const sourceMapSupport = require('source-map-support'); // eslint-disable-line
  sourceMapSupport.install();
}

if (process.env.NODE_ENV === 'development') {
  require('electron-debug')(); // eslint-disable-line global-require
  const path = require('path'); // eslint-disable-line
  const p = path.join(__dirname, '..', 'app', 'node_modules'); // eslint-disable-line
  require('module').globalPaths.push(p); // eslint-disable-line
}

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') app.quit();
});


const installExtensions = async () => {
  if (process.env.NODE_ENV === 'development') {
    const installer = require('electron-devtools-installer'); // eslint-disable-line global-require

    const extensions = [
      'REACT_DEVELOPER_TOOLS',
      'REDUX_DEVTOOLS'
    ];
    const forceDownload = !!process.env.UPGRADE_EXTENSIONS;
    for (const name of extensions) { // eslint-disable-line
      try {
        await installer.default(installer[name], forceDownload);
      } catch (e) {} // eslint-disable-line
    }
  }
};

